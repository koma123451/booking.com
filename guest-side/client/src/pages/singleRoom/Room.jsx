import "./room.css"
import Navbar from "../../components/navbar/Navbar"
import Header from "../../components/header/Header"
import MailList from "../../components/mailList/MailList"
import Footer from "../../components/footer/Footer"
import { FontAwesomeIcon } from "@fortawesome/react-fontawesome"
import { faCircleArrowLeft, faCircleArrowRight, faCircleXmark, faLocationDot } from "@fortawesome/free-solid-svg-icons"
import { useContext, useState } from "react"
import useFetch from "../../hooks/useFetch";
import { useLocation, useNavigate } from "react-router-dom"
import { SearchContext } from "../../context/SearchContext"
import { AuthContext } from "../../context/AuthContext"
import Reserve from "../../components/reserve/Reserve"
const Room = () => {
  const location = useLocation()
  const id = location.pathname.split("/")[2];
  const [slideNumber,setSlideNumber]=useState(0);
  const [open,setOpen]=useState(false);
  const [openModal,setOpeModal]=useState(false);



  const { data, loading, error, reFetch } = useFetch(
    `http://localhost:8800/api/hotels/find/${id}`
  );
  const {user} = useContext(AuthContext);
  const  navigate = useNavigate();
  const { dates,options} = useContext(SearchContext);

  const MILLISECONDS_PER_DAY = 1000 * 60 * 60 * 24;
  function dayDifference(date1, date2) {
    const timeDiff = Math.abs(date2.getTime() - date1.getTime());
    const diffDays = Math.ceil(timeDiff / MILLISECONDS_PER_DAY);
    return diffDays;
  }

  const days = dayDifference(dates[0].endDate, dates[0].startDate);


  const handleOpen = (i)=>{
    setSlideNumber(i);
    setOpen(true);
  }

  const handleMove =(direction)=>{
    let newSlidesNumber;

    if(direction ==="l"){
      newSlidesNumber= slideNumber === 0 ? 5 : slideNumber-1;
    }
    else{
      newSlidesNumber= slideNumber === 5 ? 0 : slideNumber+1;
    }
    setSlideNumber(newSlidesNumber)
  };
  const handleClick =() =>{
    if(user){
      setOpeModal(true)

    }else{
      navigate("/login")
    }

  }
  return (
    <div>
      <Navbar/>
      <Header type="list"/>
      {loading ? (
        "loading"
      ):(
        <div className="roomContainer">
        {open && <div className="slider">
      <FontAwesomeIcon icon={faCircleXmark} className="close" onClick={()=>setOpen(false)}/>
      <FontAwesomeIcon icon={faCircleArrowLeft} className="arrow" onClick={()=>handleMove("l")}/>
      <div className="sliderWrapper">
        <img src={data.photos[slideNumber]} alt="" className="slideImg" />
      </div>
      <FontAwesomeIcon icon={faCircleArrowRight} className="arrow" onClick={()=>handleMove("r")}/>
        </div>}
        <div className="roomWrapper">
          <button className="bookNow">Reserve or Book Now!</button>
          <h1 className="roomTitle">{data.name}</h1>
          <div className="roomAddress">
            <FontAwesomeIcon icon={faLocationDot}/>
            <span>941 Progress Ave</span>
          </div>
          <span className="roomDistance">
            Excellent location - {data.distance}m from center
          </span>
          <span className="roomPriceHighlight">
            Book a stay over ${data.cheapestPrice} at this property and get a free airport taxi
          </span>
          <div className="roomImages">
    {data.photos?.map((photo, i)=>(
      <div className="roomImgWrapper">
        <img onClick={()=>handleOpen(i)} src={photo} alt="" className="roomImg" />
      </div>
    ))}
          </div>
          <div className="roomDetails">
            <div className="roomDetailsTexts">
            <h1 className="roomTitle">{data.title}</h1>
            <p className="roomDesc">
              {data.desc}
            </p>
            </div>
            <div className="roomDetailsPrice">
              <h1>Perfect for a {days}-night stay!</h1>
              <span>
              The Motel Olympia is one mile from the famous Horseshoe Falls. The hotel is within a 14-minute walk of Skylon Towers, which features dining, shopping and views of the Niagara Falls.
              </span>
              <h2>
                <b>${days * data.cheapestPrice * options.room} ({days}nights)</b>
              </h2>
              <button onClick={handleClick}>Reserve or Book Now!</button>
            </div>
          </div>
        </div>
        <MailList/>
        <Footer/>
      </div>
)}
{openModal && <Reserve setOpen={setOpeModal} hotelId={id}/>}
    </div>
  )
}

export default Room
